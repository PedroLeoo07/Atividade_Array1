//Metodo utilizado ForEach

let numeros = [10, 12, 14, 16, 5];
//let /criada para verificação dos numeros

numeros.forEach(function (elemento, índice) {
//Verificação para ver a posição do número

    console.log(`o número ${elemento} está na posição ${índice}`);
});
